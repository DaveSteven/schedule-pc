// 根据路径获取模拟数据的函数
export function getMockDataByPath(path: string, params?: any) {
  const mockData = mockDataConfig[path as keyof typeof mockDataConfig];

  if (!mockData) {
    return {
      status: "1",
      message: "接口不存在",
      data: null,
    };
  }

  // 如果是查询日程接口，根据参数过滤数据
  if (path === "/api/schedule/staff/querySchedule" && params) {
    const { startDate, endDate } = params;
    const originalData = mockData.data;

    console.log("=== Mock数据过滤调试 ===");
    console.log("请求路径:", path);
    console.log("请求参数:", params);
    console.log("原始数据长度:", originalData?.length || 0);
    console.log("日期范围:", { startDate, endDate });

    if (Array.isArray(originalData)) {
      const filteredData = originalData.filter((event: any) => {
        // 确保事件对象包含 startDate 和 endDate 属性
        if (!event.startDate || !event.endDate) {
          console.log("事件缺少日期信息:", event);
          return false;
        }

        const eventStart = dayjs(event.startDate);
        const eventEnd = dayjs(event.endDate);
        const rangeStart = dayjs(startDate);
        const rangeEnd = dayjs(endDate);

        const isInRange =
          (eventStart.isSameOrAfter(rangeStart) &&
            eventStart.isSameOrBefore(rangeEnd)) ||
          (eventEnd.isSameOrAfter(rangeStart) &&
            eventEnd.isSameOrBefore(rangeEnd)) ||
          (eventStart.isSameOrBefore(rangeStart) &&
            eventEnd.isSameOrAfter(rangeEnd));

        return isInRange;
      });

      return {
        ...mockData,
        data: filteredData,
      };
    }
  }

  return mockData;
}

